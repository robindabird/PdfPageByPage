namespace Converters
{
    /// <summary>
    /// Class representing the diverse extension files
    /// </summary>
    public static class FileExtension
    {
        /// <summary>
        /// Get the extension of a PDF file
        /// </summary>
        public const string PDF = ".pdf";

        /// <summary>
        /// Get the extension of a XLS file
        /// </summary>
        public const string XLS = ".xls";

        /// <summary>
        /// Get the extension of a XLSX file
        /// </summary>
        public const string XLSX = ".xlsx";

        /// <summary>
        /// Get the extension of an ODS file
        /// </summary>
        public const string ODS = ".ods";
    }
}
